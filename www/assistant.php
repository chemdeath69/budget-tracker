            your real data. To enable it, add an <strong>Anthropic API key</strong> to
            <code>lib/config.php</code> (the <code>anthropic.api_key</code> setting) — the same key
            the 401(k) photo importer uses. It runs on pay-as-you-go Anthropic credits (a few cents
            per question) and is read-only: it can look things up but never change anything.
