            <button type="submit" id="assistant-send" class="btn assistant-send" aria-label="Send">Ask</button>
        </form>
        <p class="muted assistant-disclaimer">
            Answers are generated by AI from your data and may be imperfect — double-check anything important.
        </p>
    </section>
<?php endif; ?>

